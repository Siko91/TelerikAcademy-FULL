
namespace T4Template
{
    class VeryStrangeShape
    {
		
			public int Item0X { get; set; }
			public int Item0Y { get; set; }
			public int Item0Z { get; set; }

		
			public int Item1X { get; set; }
			public int Item1Y { get; set; }
			public int Item1Z { get; set; }

		
			public int Item2X { get; set; }
			public int Item2Y { get; set; }
			public int Item2Z { get; set; }

		
			public int Item3X { get; set; }
			public int Item3Y { get; set; }
			public int Item3Z { get; set; }

		
			public int Item4X { get; set; }
			public int Item4Y { get; set; }
			public int Item4Z { get; set; }

		
			public int Item5X { get; set; }
			public int Item5Y { get; set; }
			public int Item5Z { get; set; }

		
			public int Item6X { get; set; }
			public int Item6Y { get; set; }
			public int Item6Z { get; set; }

		
        public VeryStrangeShape(int centerPoint, int size)
        {
			
			Item0X = centerPoint + size/1;
			Item0Y = centerPoint + size/1;
			Item0Z = centerPoint + size/1;

			
			Item1X = centerPoint + size/2;
			Item1Y = centerPoint + size/2;
			Item1Z = centerPoint + size/2;

			
			Item2X = centerPoint + size/3;
			Item2Y = centerPoint + size/3;
			Item2Z = centerPoint + size/3;

			
			Item3X = centerPoint + size/4;
			Item3Y = centerPoint + size/4;
			Item3Z = centerPoint + size/4;

			
			Item4X = centerPoint + size/5;
			Item4Y = centerPoint + size/5;
			Item4Z = centerPoint + size/5;

			
			Item5X = centerPoint + size/6;
			Item5Y = centerPoint + size/6;
			Item5Z = centerPoint + size/6;

			
			Item6X = centerPoint + size/7;
			Item6Y = centerPoint + size/7;
			Item6Z = centerPoint + size/7;

			        }
    }
}
